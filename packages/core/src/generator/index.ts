function getInstallCommand(pm: DetectedProjectInfo["packageManager"]): string {
  switch (pm) {
    case "pnpm": return "pnpm install";
    case "yarn": return "yarn install";
    case "bun": return "bun install";
    default: return "npm install";
  }
}
